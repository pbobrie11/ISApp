import Foundation
import UIKit

// Color palette

extension UIColor {
    class func charcoalGray() -> UIColor {
        return UIColor(red: 59.0 / 255.0, green: 60.0 / 255.0, blue: 67.0 / 255.0, alpha: 1.0)
    }
    
    class func steelGray() -> UIColor {
        return UIColor(red: 148.0 / 255.0, green: 150.0 / 255.0, blue: 154.0 / 255.0, alpha: 1.0)
    }
    
    class func synchronyGold() -> UIColor {
        return UIColor(red: 251.0 / 255.0, green: 198.0 / 255.0, blue: 0.0 / 255.0, alpha: 1.0)
    }
    
    class func synchronyGreen() -> UIColor {
        return UIColor(red: 110.0 / 255.0, green: 191.0 / 255.0, blue: 74.0 / 255.0, alpha: 1.0)
    }
}
